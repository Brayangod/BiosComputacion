    private void rSButtonRiple2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonRiple2ActionPerformed
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione el producto a modificar");
        } else {
            FrmModificarProducto form = new FrmModificarProducto();

            deskPrincipal.add(form);
            Dimension desktopSize = deskPrincipal.getSize();
            Dimension FrameSize = form.getSize();

            form.setLocation((desktopSize.width - FrameSize.width) / 2, (desktopSize.height - FrameSize.height) / 2);
            form.setVisible(true);
            form.setClosable(true);
            form.setIconifiable(false);
            
            form.idProducto = Integer.parseInt(tablaProductos.getValueAt(fila, 0).toString());
            form.txtCodBarras.setText(tablaProductos.getValueAt(fila, 4).toString());
            form.txtCodProveedor.setText(tablaProductos.getValueAt(fila, 5).toString());
            form.txtNombreProducto.setText(tablaProductos.getValueAt(fila, 3).toString());
            form.txtPrecioCosto.setText(tablaProductos.getValueAt(fila, 6).toString());
            form.txtPrecioEfectivo.setText(tablaProductos.getValueAt(fila, 7).toString());
            form.txtPrecioCuotas.setText(tablaProductos.getValueAt(fila, 8).toString());
            form.txtPorcentajeGanancias.setText(tablaProductos.getValueAt(fila, 9).toString());
            form.txtStock.setText(tablaProductos.getValueAt(fila, 10).toString());
            form.cbRubro.setSelectedItem(tablaProductos.getValueAt(fila, 1).toString());
            if(tablaProductos.getValueAt(fila, 2)!=null){
                form.cbSubRubro.setSelectedItem(tablaProductos.getValueAt(fila, 2).toString());
            }
            btnActualizar.doClick();
        }
    }//GEN-LAST:event_rSButtonRiple2ActionPerformed

    private void rSButtonRiple4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonRiple4ActionPerformed
 
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione el producto a eliminar");
        }
        else{
            if(JOptionPane.showConfirmDialog(null, "Esta seguro que desea eliminar el producto?", "ELIMINACION DE PRODUCTO", JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE) == 0){
                ProductoDAO productoDao = new ProductoDAO();
                Producto producto = new Producto();
                producto.setIdProducto(Integer.parseInt(tablaProductos.getValueAt(fila, 0).toString()));
                if(productoDao.eliminar(producto)){
                    JOptionPane.showMessageDialog(null, "El producto ha sido eliminado satisfactoriamente");
                }
                else{
                    JOptionPane.showMessageDialog(null, "OcurriÃ³ un error al eliminar el producto");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Movimiento cancelado");
            }
            btnActualizar.doClick();
        }
    }//GEN-LAST:event_rSButtonRiple4ActionPerformed

